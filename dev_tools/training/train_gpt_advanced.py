class LoRALayer(nn.Module):
    """Low-Rank Adaptation layer for efficient fine-tuning."""
    def __init__(self, in_features: int, out_features: int, rank: int, alpha: float):
        super().__init__()
        self.rank = rank
        self.alpha = alpha
        self.lora_A = nn.Parameter(torch.zeros(in_features, rank, dtype=torch.float32))
        self.lora_B = nn.Parameter(torch.zeros(rank, out_features, dtype=torch.float32))
        self.scale = alpha / rank
        nn.init.kaiming_uniform_(self.lora_A, a=math.sqrt(5))
        nn.init.zeros_(self.lora_B)
    
    def forward(self, x: Tensor, base_weight: Tensor) -> Tensor:
        # x: [..., in_features], base_weight: [out_features, in_features]
        # LoRA path: x @ (A @ B) * scale
        lora_update = (x @ self.lora_A.to(x.dtype)) @ self.lora_B.to(x.dtype)
        base_out = F.linear(x, base_weight.to(x.dtype))
        return base_out + lora_update * self.scale


class LoRAModel(nn.Module):
    """Wraps base model with LoRA layers for test-time training."""
    def __init__(self, base_model: nn.Module, rank: int, alpha: float, num_layers_to_adapt: int = 2):
        super().__init__()
        self.base_model = base_model
        self.rank = rank
        self.alpha = alpha
        self.lora_layers: dict[str, LoRALayer] = nn.ModuleDict()
        
        # Add LoRA to last few MLP projections (most impactful)
        for i in range(max(0, base_model.num_encoder_layers + base_model.num_decoder_layers - num_layers_to_adapt),
                       base_model.num_encoder_layers + base_model.num_decoder_layers):
            block = base_model.blocks[i]
            # LoRA for MLP projection
            dim = block.mlp.proj.weight.shape[1]  # input dim
            hidden = block.mlp.proj.weight.shape[0]  # output dim
            self.lora_layers[f"block_{i}_mlp_proj"] = LoRALayer(dim, hidden, rank, alpha)
    
    def forward(self, input_ids: Tensor, target_ids: Tensor) -> Tensor:
        # Forward with LoRA applied
        x = self.base_model.tok_emb(input_ids)
        if self.base_model.bigram is not None:
            x = x + self.base_model.bigram(input_ids)
        x = F.rms_norm(x, (x.size(-1),))
        x = self.base_model.smear(x)
        x0 = x
        skips: list[Tensor] = []
        
        for i in range(self.base_model.num_encoder_layers):
            x = self.base_model.blocks[i](x, x0)
            skips.append(x)
        for i in range(self.base_model.num_decoder_layers):
            if skips:
                x = x + self.base_model.skip_weights[i].to(dtype=x.dtype)[None, None, :] * skips.pop()
            block = self.base_model.blocks[self.base_model.num_encoder_layers + i]
            
            # Apply LoRA to selected layers
            lora_key = f"block_{self.base_model.num_encoder_layers + i}_mlp_proj"
            if lora_key in self.lora_layers:
                # Custom forward with LoRA
                mix = block.resid_mix.to(dtype=x.dtype)
                x = mix[0][None, None, :] * x + mix[1][None, None, :] * x0
                attn_out = block.attn(block.attn_norm(x))
                x = x + block.attn_scale.to(dtype=x.dtype)[None, None, :] * attn_out
                # MLP with LoRA
                mlp_out = torch.relu(block.mlp.fc(x))
                mlp_out = mlp_out.square()
                mlp_proj = self.lora_layers[lora_key](mlp_out, block.mlp.proj.weight)
                x = x + block.mlp_scale.to(dtype=x.dtype)[None, None, :] * mlp_proj
            else:
                x = block(x, x0)
        
        x = self.base_model.final_norm(x).reshape(-1, x.size(-1))
        targets = target_ids.reshape(-1)
        if self.base_model.tie_embeddings:
            logits_proj = F.linear(x, self.base_model.tok_emb.weight)
        else:
            logits_proj = self.base_model.lm_head(x)
        logits = self.base_model.logit_softcap * torch.tanh(logits_proj / self.base_model.logit_softcap)
        return F.cross_entropy(logits.float(), targets, reduction="mean")


