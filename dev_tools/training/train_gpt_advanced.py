def quantize_intN_per_row(t: Tensor, clip_range: int) -> tuple[Tensor, Tensor]:
    t32 = t.float()
    if t32.ndim == 2:
        row_max = t32.abs().amax(dim=1)
        scale = (row_max / clip_range).clamp_min(1e-12).to(torch.float16)
        scale = scale.clamp_min(torch.finfo(torch.float16).tiny)
        q = torch.clamp(torch.round(t32 / scale.unsqueeze(1)), -clip_range, clip_range).to(torch.int8)
        return q, scale
    max_val = t32.abs().max()
    scale = torch.tensor([(max_val / clip_range).clamp_min(1e-12)], dtype=torch.float16)
    q = torch.clamp(torch.round(t32 / scale), -clip_range, clip_range).to(torch.int8)
    return q, scale

def quantize_float_tensor(t: Tensor) -> tuple[Tensor, Tensor]:
    t32 = t.float()
    if t32.ndim == 2:
        row_max = t32.abs().amax(dim=1)
        scale = (row_max / 127.0).clamp_min(1e-12).to(torch.float16)
        scale = scale.clamp_min(torch.finfo(torch.float16).tiny)
        q = torch.clamp(torch.round(t32 / scale.unsqueeze(1)), -127, 127).to(torch.int8)
        return q, scale
    max_val = t32.abs().max()
    scale = torch.tensor([(max_val / 127.0).clamp_min(1e-12)], dtype=torch.float16)
    q = torch.clamp(torch.round(t32 / scale), -127, 127).to(torch.int8)
    return q, scale

def mixed_quantize_advanced(
    state_dict: dict[str, Tensor], 
    mlp_fc_bits: int, 
    mlp_proj_bits: int,
    attn_bits: int
):
    result: dict[str, Tensor] = {}
    meta: dict[str, object] = {}
    for name, tensor in state_dict.items():
        t = tensor.detach().cpu().contiguous()
        cat = _classify_param(name)
        if any(p in name for p in FP16_KEEP_NAME_PATTERNS) or t.numel() <= 65536:
            result[name] = t.to(torch.float16)
            meta[name] = "passthrough_fp16"
            continue
        if cat == "mlp_fc":
            clip = (1 << (mlp_fc_bits - 1)) - 1  # 7 for int4
            q, s = quantize_intN_per_row(t, clip_range=clip)
            result[name + ".q"] = q
            result[name + ".scale"] = s
            meta[name] = {"type": f"int{mlp_fc_bits}"}
        elif cat == "mlp_proj":
            clip = (1 << (mlp_proj_bits - 1)) - 1  # 15 for int5
            q, s = quantize_intN_per_row(t, clip_range=clip)
            result[name + ".q"] = q
            result[name + ".scale"] = s
            meta[name] = {"type": f"int{mlp_proj_bits}"}
        elif cat == "attn":
            clip = (1 << (attn_bits - 1)) - 1  # 31 for int6
            q, s = quantize_intN_per_row(t, clip_range=clip)
            result[name + ".q"] = q
            result[name + ".scale"] = s
            meta[name] = {"type": f"int{attn_bits}"}
        else:
            q, s = quantize_float_tensor(t)
            result[name + ".q"] = q
            result[name + ".scale"] = s
            meta[name] = {"type": "int8"}
    return result, meta

def dequantize_mixed(result: dict[str, Tensor], meta: dict[str, object], template_sd: dict[str, Tensor]) -> dict[str, Tensor]:
    out: dict[str, Tensor] = {}
    for name, orig in template_sd.items():
        info = meta[name]
        orig_dtype = orig.dtype
        if info == "passthrough_fp16":
            out[name] = result[name].to(orig_dtype)
        else:
            q = result[name + ".q"]
            s = result[name + ".scale"].to(torch.float32)
            if s.ndim > 0:
                out[name] = (q.float() * s.unsqueeze(1)).to(orig_dtype)
            else:
                out[name] = (q.float() * s.item()).to(orig_dtype)
    return out


