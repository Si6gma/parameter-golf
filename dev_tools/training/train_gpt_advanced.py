def eval_val_sliding_with_ttt(
    args: Hyperparameters,
    base_model: GPT,
    rank: int,
    world_size: int,
    device: torch.device,
    val_tokens: Tensor,
    base_bytes_lut: Tensor,
    has_leading_space_lut: Tensor,
    is_boundary_token_lut: Tensor,
    stride: int,
    batch_seqs: int = 32,
) -> tuple[float, float]:
    """Sliding window evaluation with test-time training on previous tokens."""
    seq_len = args.train_seq_len
    total_tokens = val_tokens.numel() - 1
    window_starts = list(range(0, total_tokens - seq_len, stride))
    num_windows = len(window_starts)
    
    ws_start = (num_windows * rank) // world_size
    ws_end = (num_windows * (rank + 1)) // world_size
    my_windows = window_starts[ws_start:ws_end]
    
    # Create LoRA model for TTT
    if args.ttt_enabled:
        lora_model = LoRAModel(base_model, args.ttt_lora_rank, args.ttt_lora_alpha).to(device)
        # Get trainable params
        ttt_params = [p for p in lora_model.parameters() if p.requires_grad]
        ttt_optimizer = torch.optim.Adam(ttt_params, lr=args.ttt_lr, betas=(0.9, 0.999))
    
    total_loss = 0.0
    total_count = 0
    total_bytes = 0.0
    
    base_model.eval()
    prev_context_tokens: list[Tensor] = []
    
    with torch.inference_mode():
        for bi, ws in enumerate(my_windows):
            # TTT: Fine-tune on previous context
            if args.ttt_enabled and len(prev_context_tokens) > 0 and bi % 10 == 0:
                lora_model.train()
                for _ in range(args.ttt_steps):
                    ttt_optimizer.zero_grad()
                    # Use recent context for TTT
                    ctx = prev_context_tokens[-1][:512].unsqueeze(0)  # Short context for efficiency
                    with torch.enable_grad():
                        loss = lora_model(ctx[:, :-1], ctx[:, 1:])
                    loss.backward()
                    ttt_optimizer.step()
                lora_model.eval()
            
            end = min(ws + seq_len, total_tokens)
            wlen = end - ws
            chunk = val_tokens[ws:end + 1].to(dtype=torch.int64, device=device)
            x = chunk[:-1].unsqueeze(0)
            y = chunk[1:].unsqueeze(0)
            
            with torch.autocast(device_type="cuda", dtype=torch.bfloat16):
                if args.ttt_enabled:
                    loss = lora_model(x, y)
                else:
                    # Standard forward
                    emb = base_model.tok_emb(x)
                    if base_model.bigram is not None:
                        emb = emb + base_model.bigram(x)
                    emb = F.rms_norm(emb, (emb.size(-1),))
                    emb = base_model.smear(emb)
                    x0 = emb
                    skips = []
                    for i in range(base_model.num_encoder_layers):
                        emb = base_model.blocks[i](emb, x0)
                        skips.append(emb)
                    for i in range(base_model.num_decoder_layers):
                        if skips:
                            emb = emb + base_model.skip_weights[i].to(dtype=emb.dtype)[None, None, :] * skips.pop()
                        emb = base_model.blocks[base_model.num_encoder_layers + i](emb, x0)
                    emb = base_model.final_norm(emb)
                    logits_proj = F.linear(emb, base_model.tok_emb.weight)
                    logits = base_model.logit_softcap * torch.tanh(logits_proj / base_model.logit_softcap)
                    loss = F.cross_entropy(logits.float().reshape(-1, logits.size(-1)), y.reshape(-1), reduction="mean")
            
            total_loss += float(loss.item()) * wlen
            total_count += wlen
            
            prev_ids = x.reshape(-1)
            tgt_ids = y.reshape(-1)
            token_bytes = base_bytes_lut[tgt_ids].to(dtype=torch.int16)
            token_bytes += (has_leading_space_lut[tgt_ids] & ~is_boundary_token_lut[prev_ids]).to(dtype=torch.int16)
            total_bytes += float(token_bytes.to(torch.float64).sum().item())
            
            # Store context for TTT
            prev_context_tokens.append(chunk)
            if len(prev_context_tokens) > 5:
                prev_context_tokens.pop(0)
    
    if dist.is_available() and dist.is_initialized():
        total_loss_tensor = torch.tensor(total_loss, device=device, dtype=torch.float64)
        total_count_tensor = torch.tensor(total_count, device=device, dtype=torch.float64)
        total_bytes_tensor = torch.tensor(total_bytes, device=device, dtype=torch.float64)
        dist.all_reduce(total_loss_tensor, op=dist.ReduceOp.SUM)
        dist.all_reduce(total_count_tensor, op=dist.ReduceOp.SUM)
        dist.all_reduce(total_bytes_tensor, op=dist.ReduceOp.SUM)
        total_loss = total_loss_tensor.item()
        total_count = total_count_tensor.item()
        total_bytes = total_bytes_tensor.item()
    
    val_loss = total_loss / total_count
    bits_per_token = val_loss / math.log(2.0)
    tokens_per_byte = total_count / total_bytes
    base_model.train()
    return val_loss, bits_per_token * tokens_per_byte


